    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         String Id=  id.getText().toString();
         String Password = password.getText().toString();
         String sql = "select User_Id , Password_User from account";
        try{
             rst=stm.executeQuery(sql);
             int i=0;
             if(Id.equals("") || Password.equals("")){
                 if(i==0)JOptionPane.showMessageDialog(null,"Remplissez ,les Champs Vides !");
             }else{
                 while(rst.next()){
                 String Id1=  rst.getString("User_Id");
                 String Password1=   rst.getString("Password_User");
                 if(Id1.equals(Id) && Password1.equals(Password)){
                      JOptionPane.showMessageDialog(null,"Connexion R√©ussite");
                      i=1;
                      CustomerOrder C = new CustomerOrder();
                      C.setVisible(true);
                      this.dispose();
                 }
             }
             if(i==0)JOptionPane.showMessageDialog(null,"Connexion Echou√©e, Informations Incorrects");
             }
             
        }catch(Exception e){
            e.printStackTrace();
        }
        
  
    }//GEN-LAST:event_jButton1ActionPerformed

